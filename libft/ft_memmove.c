#include "libft.h"

void	*ft_memmove(void *dest, const void *src, int n)
{
	char		*d;
	const char	*s;

	d = (char *) dest;
	s = (const char *) src;
	if (s == 0 && d == 0)
		return (NULL);
	if (d <= s)
	{
		while (n--)
		{
			*d++ = *s++;
		}
	}
	else
	{
		d += n;
		s += n;
		while (n--)
			*(--d) = *(--s);
	}
	return (dest);
}
